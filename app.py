# Inicializar sesión para almacenamiento temporal
if "data" not in st.session_state:
    st.session_state.data = pd.DataFrame(columns=[
        "Código", "Producto", "Cantidad", "Precio Unitario", "Fecha",
        "Código Almacén", "Local", "Incluye IVA"
    ])

# Título
st.title("Generador de Layout para Microsoft Dynamics")

# Selección de método
option = st.radio("¿Cómo quieres ingresar los productos?", ("Captura manual", "Cargar archivo Excel"))

if option == "Captura manual":
    with st.form("form_productos"):
        producto_completo = st.selectbox(
            "🔍 Selecciona un producto (código - nombre):",
            options=productos,
            index=None,
            placeholder="Escribe parte del código o nombre..."
        )
        codigo_almacen = st.text_input("Código de almacén")
        local = st.selectbox("Selecciona el local", options=["COAPA"])
        cantidad = st.number_input("Cantidad", min_value=1, step=1)
        precio = st.number_input("Precio Unitario", min_value=0.01, step=0.01)
        incluye_iva = st.checkbox("¿El producto incluye IVA?")
        fecha = st.date_input("Fecha de compra", value=datetime.today())
        submitted = st.form_submit_button("Agregar")

        if submitted:
            if producto_completo:
                codigo, nombre = producto_completo.split(" - ", 1)
                if incluye_iva:
                    precio_total = precio * 1.16
                else:
                    precio_total = precio
                new_row = {
                    "Código": codigo.strip(),
                    "Producto": nombre.strip(),
                    "Cantidad": cantidad,
                    "Precio Unitario": precio_total,
                    "Fecha": fecha,
                    "Código Almacén": codigo_almacen.strip(),
                    "Local": local,
                    "Incluye IVA": "Sí" if incluye_iva else "No"
                }
                st.session_state.data = pd.concat([st.session_state.data, pd.DataFrame([new_row])], ignore_index=True)
                st.success("Producto agregado correctamente.")
            else:
                st.warning("Por favor selecciona un producto válido.")

elif option == "Cargar archivo Excel":
    uploaded_file = st.file_uploader("Cargar archivo Excel", type=["xlsx"])
    if uploaded_file:
        try:
            df = pd.read_excel(uploaded_file)
            required_cols = ["Código", "Producto", "Cantidad", "Precio Unitario", "Fecha", "Código Almacén", "Local"]
            if all(col in df.columns for col in required_cols):
                st.session_state.data = df
                st.success("Archivo cargado correctamente.")
            else:
                st.error("El archivo debe contener las columnas: Código, Producto, Cantidad, Precio Unitario, Fecha, Código Almacén, Local")
        except Exception as e:
            st.error(f"Error al leer el archivo: {e}")

# Mostrar vista previa si hay datos
if not st.session_state.data.empty:
    df = st.session_state.data.copy()
    df["Fecha"] = pd.to_datetime(df["Fecha"], errors="coerce")

    if df["Fecha"].isnull().any():
        st.error("Hay fechas inválidas en los datos. Verifica el formato de la columna 'Fecha'.")
    else:
        df["Costo Extendido"] = df["Cantidad"] * df["Precio Unitario"]

        st.subheader("📋 Vista previa de los productos capturados")
        st.dataframe(df)

        if st.button("📝 Generar archivo layout_dynamics.xlsx"):
            layout = pd.DataFrame()
            layout["A"] = df["Código"]
            layout["B"] = df["Código Almacén"]
            layout["C"] = df["Cantidad"]
            layout["D"] = ""
            layout["E"] = ""
            layout["F"] = df["Costo Extendido"]
            layout["G"] = ""
            layout["H"] = ""
            layout["I"] = ""
            layout["J"] = ""
            layout["K"] = df["Fecha"].dt.strftime("%d/%m/%Y")
            layout["L"] = df["Fecha"].dt.strftime("%d/%m/%Y")
            for col in ["M", "N", "O", "P", "Q", "R", "S", "T"]:
                layout[col] = ""
            layout["U"] = df["Producto"]
            layout["V"] = df["Local"]

            output_path = os.path.join(SAVE_FOLDER, "layout_dynamics.xlsx")
            layout.to_excel(output_path, index=False, engine="openpyxl")
            st.success(f"Archivo guardado en: {output_path}")
            with open(output_path, "rb") as f:
                st.download_button("⬇️ Descargar archivo", f, file_name="layout_dynamics.xlsx")

    if st.button("🗑️ Limpiar datos capturados"):
        st.session_state.data = pd.DataFrame(columns=[
            "Código", "Producto", "Cantidad", "Precio Unitario", "Fecha",
            "Código Almacén", "Local", "Incluye IVA"
        ])
        st.success("Datos limpiados.")
